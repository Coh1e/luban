// ---- powershell ------------------------------------------------------------

// PowerShell uses Register-ArgumentCompleter. Drop the script in your
// $PROFILE or a module so it's loaded at shell start.
//
// Built in three concat'd pieces so the verb-list interpolation doesn't
// require breaking out of (and re-entering) a raw string literal — that
// trick is fragile because `)PWSH"` must match exactly and copy-paste
// errors silently leak C++ source text into the output.
std::string emit_powershell() {
    constexpr std::string_view kHead = R"PWSH(# luban tab completion for PowerShell (5.1+ / pwsh 7+).
# Generated by `luban completion powershell`. Add to your $PROFILE:
#   luban completion powershell | Out-String | Invoke-Expression
# (or save the output to a .ps1 and dot-source it from $PROFILE).

Register-ArgumentCompleter -Native -CommandName luban -ScriptBlock {
    param($wordToComplete, $commandAst, $cursorPosition)

    $tokens = $commandAst.CommandElements | ForEach-Object { $_.ToString() }
    $verb = if ($tokens.Count -ge 2) { $tokens[1] } else { '' }

    $verbs = @()PWSH";

    constexpr std::string_view kTail = R"PWSH()

    if ($commandAst.CommandElements.Count -le 2 -or
        ($commandAst.CommandElements.Count -eq 2 -and -not $wordToComplete.StartsWith('-'))) {
        # Position 1 — verb completion.
        $verbs | Where-Object { $_ -like "$wordToComplete*" } | ForEach-Object {
            [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_)
        }
        return
    }

    $candidates = @()
    switch ($verb) {
        'new'        { $candidates = @('--no-build','--at','--target','--with-specs','app','lib') }
        'target'     { $candidates = @('add','remove','lib','exe') }
        'self'       { $candidates = @('--yes','--keep-data','update','uninstall') }
        'env'        { $candidates = @('--user','--unset-user','--print','--shell','--msvc-init','--msvc-clear','--arch') }
        'setup'      { $candidates = @('--only','--with','--without','--force','--dry-run') }
        'describe'   { $candidates = @('--json','--host') }
        'doctor'     { $candidates = @('--strict','--json') }
        'build'      { $candidates = @('--preset','--at') }
        'doc'        { $candidates = @('--open','--clean') }
        'specs'      { $candidates = @('init','sync','new','status') }
        'shim'       { $candidates = @('--force') }
        'completion' { $candidates = @('clink','bash','zsh','fish','powershell') }
        default      { return }
    }
    $candidates | Where-Object { $_ -like "$wordToComplete*" } | ForEach-Object {
        [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_)
    }
}
)PWSH";

    std::ostringstream s;
    s << kHead;
    s << verbs_pwsh_quoted();   // 'add', 'build', 'completion', ... (commas required)
    s << kTail;
    return s.str();
}

// ---- dispatch --------------------------------------------------------------

int run_completion(const cli::ParsedArgs& a) {
    if (a.positional.empty()) {
        log::err("usage: luban completion <shell>");
        log::info("supported: clink | bash | zsh | fish | powershell");
        return 2;
    }
    const std::string& shell = a.positional[0];

    if (shell == "clink") {
        std::cout << kClinkHead << verbs_clink_quoted() << kClinkTail;
        return 0;
    }
    if (shell == "bash")       { std::cout << emit_bash(); return 0; }
    if (shell == "zsh")        { std::cout << emit_zsh(); return 0; }
    if (shell == "fish")       { std::cout << emit_fish(); return 0; }
    if (shell == "powershell" || shell == "pwsh") { std::cout << emit_powershell(); return 0; }

    log::errf("unsupported shell: {}", shell);
    log::info("supported: clink | bash | zsh | fish | powershell");
    return 2;
}

