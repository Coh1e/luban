void register_completion() {
    cli::Subcommand c;
    c.name = "completion";
    c.help = "emit shell completion script (clink / bash / zsh / fish / powershell)";
    c.group = "advanced";
    c.long_help =
        "  Print a shell-specific tab-completion script to stdout.\n"
        "\n"
        "  clink       Lua argmatcher script for cmd.exe with clink loaded.\n"
        "              Pipe to %LOCALAPPDATA%\\clink\\completions\\luban.lua.\n"
        "  bash        compgen-based completion. Source from .bash_completion\n"
        "              or drop under /etc/bash_completion.d/.\n"
        "  zsh         _arguments-based completion. Save as `_luban` in any\n"
        "              dir on $fpath, then `compinit`.\n"
        "  fish        Per-line `complete` script. Save under\n"
        "              ~/.config/fish/completions/luban.fish.\n"
        "  powershell  Register-ArgumentCompleter script. Dot-source from\n"
        "              $PROFILE or `Out-String | Invoke-Expression` it.\n"
        "\n"
        "  Verb / flag list is hardcoded in the script — re-run after\n"
        "  upgrading luban to refresh.";
    c.n_positional = 1;
    c.positional_names = {"shell"};
    c.examples = {
        "luban completion clink > %LOCALAPPDATA%\\clink\\completions\\luban.lua\tInstall for clink",
        "luban completion bash > ~/.local/share/bash-completion/completions/luban\tInstall for bash",
        "luban completion fish > ~/.config/fish/completions/luban.fish\tInstall for fish",
        "luban completion powershell | Out-String | Invoke-Expression\tLoad in current pwsh session",
    };
    c.run = run_completion;
    cli::register_subcommand(std::move(c));
}

