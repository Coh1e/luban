// Top-level verb list. Keep alphabetical so each shell's emitter can re-use
// the same string without per-shell ordering quirks. Drives every script
// below; bumping this here updates all 5 shells.
constexpr std::string_view kVerbsSpaceSep =
    "add build completion describe doc doctor env new remove run "
    "search self setup shim specs sync target which";

// Verbs as `'a' 'b' 'c'` (space-separated, single-quoted). Used by zsh's
// _values which takes a space-separated list of completion options.
std::string verbs_zsh_quoted() {
    std::string s(kVerbsSpaceSep);
    std::string out;
    out.reserve(s.size() + 64);
    size_t start = 0;
    bool first = true;
    while (start <= s.size()) {
        size_t end = s.find(' ', start);
        if (end == std::string::npos) end = s.size();
        if (!first) out += " ";
        first = false;
        out += "'" + s.substr(start, end - start) + "'";
        if (end == s.size()) break;
        start = end + 1;
    }
    return out;
}

// Verbs as `'a', 'b', 'c'` (comma-separated, single-quoted). Used by
// PowerShell's array literal `@('a', 'b', 'c')`. Same data, different
// delimiter.
std::string verbs_pwsh_quoted() {
    std::string s(kVerbsSpaceSep);
    std::string out;
    out.reserve(s.size() + 64);
    size_t start = 0;
    bool first = true;
    while (start <= s.size()) {
        size_t end = s.find(' ', start);
        if (end == std::string::npos) end = s.size();
        if (!first) out += ", ";
        first = false;
        out += "'" + s.substr(start, end - start) + "'";
        if (end == s.size()) break;
        start = end + 1;
    }
    return out;
}

// ---- clink (cmd.exe + clink) -----------------------------------------------

// Verbs as `"a", "b", "c"` (comma + double-quoted) for Lua table literal.
// Same data as kVerbsSpaceSep, just delimited differently. Sharing the source
// keeps clink in sync with bash/zsh/fish/pwsh — adding a new verb only
// requires touching kVerbsSpaceSep above.
std::string verbs_clink_quoted() {
    std::string s(kVerbsSpaceSep);
    std::string out;
    out.reserve(s.size() + 64);
    size_t start = 0;
    bool first = true;
    while (start <= s.size()) {
        size_t end = s.find(' ', start);
        if (end == std::string::npos) end = s.size();
        if (!first) out += ", ";
        first = false;
        out += "\"" + s.substr(start, end - start) + "\"";
        if (end == s.size()) break;
        start = end + 1;
    }
    return out;
}

