// ---- bash ------------------------------------------------------------------

// Bash completion. The function name `_luban` is conventional; the trailing
// `complete -F _luban luban` registers it. We use compgen for the verb list
// and per-verb flag fanout via a case statement.
std::string emit_bash() {
    std::ostringstream s;
    s << R"BASH(# luban tab completion for bash.
# Generated by `luban completion bash`. Source manually or drop under
# /etc/bash_completion.d/, ~/.bash_completion.d/, or
# ~/.local/share/bash-completion/completions/luban.

_luban() {
    local cur prev verbs
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    verbs=")BASH";
    s << kVerbsSpaceSep << R"BASH("

    # Position 1: top-level verb (or top-level flags).
    if [ "$COMP_CWORD" -eq 1 ]; then
        if [[ "$cur" == -* ]]; then
            COMPREPLY=( $(compgen -W "-V --version -h --help -v --verbose" -- "$cur") )
        else
            COMPREPLY=( $(compgen -W "$verbs" -- "$cur") )
        fi
        return 0
    fi

    # The verb is COMP_WORDS[1]. Branch on it for per-verb flags / positional.
    local verb="${COMP_WORDS[1]}"
    case "$verb" in
        new)        COMPREPLY=( $(compgen -W "--no-build --at --target --with-specs app lib" -- "$cur") );;
        target)     COMPREPLY=( $(compgen -W "add remove lib exe" -- "$cur") );;
        self)       COMPREPLY=( $(compgen -W "--yes --keep-data update uninstall" -- "$cur") );;
        env)        COMPREPLY=( $(compgen -W "--user --unset-user --print --shell --msvc-init --msvc-clear --arch bash cmd powershell" -- "$cur") );;
        setup)      COMPREPLY=( $(compgen -W "--only --with --without --force --dry-run" -- "$cur") );;
        describe)   COMPREPLY=( $(compgen -W "--json --host" -- "$cur") );;
        doctor)     COMPREPLY=( $(compgen -W "--strict --json" -- "$cur") );;
        build)      COMPREPLY=( $(compgen -W "--preset --at" -- "$cur") );;
        doc)        COMPREPLY=( $(compgen -W "--open --clean" -- "$cur") );;
        specs)      COMPREPLY=( $(compgen -W "init sync new status" -- "$cur") );;
        shim)       COMPREPLY=( $(compgen -W "--force" -- "$cur") );;
        completion) COMPREPLY=( $(compgen -W "clink bash zsh fish powershell" -- "$cur") );;
        *)          COMPREPLY=();;
    esac
}
complete -F _luban luban
)BASH";
    return s.str();
}

// ---- zsh -------------------------------------------------------------------

// Zsh's _arguments DSL is rich but verbose. We use a compdef function and
// per-verb subcommand definitions via _luban_<verb> helpers. Drop the file
// under any dir on $fpath named `_luban` (or autoload-on-first-use via
// `compinit`).
std::string emit_zsh() {
    std::ostringstream s;
    s << R"ZSH(#compdef luban
# luban tab completion for zsh.
# Generated by `luban completion zsh`. Save to a file named `_luban` in any
# directory on $fpath (e.g., ~/.zsh/completions/_luban), then ensure that
# dir is added via `fpath=(~/.zsh/completions $fpath)` before `compinit`.

_luban() {
    local context state state_descr line
    typeset -A opt_args

    _arguments -C \
        '(-V --version)'{-V,--version}'[show version and exit]' \
        '(-h --help)'{-h,--help}'[show help and exit]' \
        '(-v --verbose)'{-v,--verbose}'[verbose output]' \
        '1: :_luban_verbs' \
        '*::arg:->args'

    case $state in
        args)
            case $words[1] in
                new)        _values 'flag' --no-build --at --target --with-specs app lib ;;
                target)     _values 'arg' add remove lib exe ;;
                self)       _values 'flag' --yes --keep-data update uninstall ;;
                env)        _values 'flag' --user --unset-user --print --shell --msvc-init --msvc-clear --arch ;;
                setup)      _values 'flag' --only --with --without --force --dry-run ;;
                describe)   _values 'flag' --json --host ;;
                doctor)     _values 'flag' --strict --json ;;
                build)      _values 'flag' --preset --at ;;
                doc)        _values 'flag' --open --clean ;;
                specs)      _values 'sub' init sync new status ;;
                shim)       _values 'flag' --force ;;
                completion) _values 'shell' clink bash zsh fish powershell ;;
            esac
            ;;
    esac
}

_luban_verbs() {
    _values 'verb' )ZSH";
    s << verbs_zsh_quoted() << R"ZSH(
}

_luban "$@"
)ZSH";
    return s.str();
}

