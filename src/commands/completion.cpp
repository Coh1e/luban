// `luban completion <shell>` — emit shell-specific tab completion script.
//
// Supported shells: clink (cmd.exe enhancement), bash, zsh, fish, powershell.
//
// Each emitter is a static string template (some with the verb list spliced
// in once for DRY-ness). Manual authoring beats runtime introspection here:
// shells load the completion script once at session start, so a stable
// string is much faster than spawning `luban` for every Tab press. The cost
// is that adding a verb / flag means updating this file too — the alternative
// would couple every shell to luban's internal Subcommand registry which is
// neither shell-friendly nor portable.
//
// To keep maintenance manageable, only verbs + their named flags are scripted.
// Positional values that would require luban-side state (e.g., listing
// installed component aliases for `luban which`) are left as default
// path-completion or unspecified; users still get verb / flag autocomplete.

