std::string warning_flags(luban_toml::WarningLevel w) {
    switch (w) {
        case luban_toml::WarningLevel::Off:
            return "";
        case luban_toml::WarningLevel::Strict:
            return "-Wall -Wextra -Wpedantic -Werror=return-type";
        case luban_toml::WarningLevel::Normal:
        default:
            return "-Wall -Wextra";
    }
}

std::string sanitizer_flags(const std::vector<std::string>& sans) {
    if (sans.empty()) return "";
    std::string list;
    for (size_t i = 0; i < sans.size(); ++i) {
        if (i) list.push_back(',');
        list += sans[i];
    }
    return "-fsanitize=" + list;
}

